// Задача 2: Вывести все простые числа от 1 до 1000

package seminar1;

public class ex2 {
    public static void main(String[] args) {
        // Перебираем все числа от 1 до 1000
        for (int i = 1; i <= 1000; i++) {
            // Проверяем, является ли число простым
            if (isPrime(i)) {
                // Если число простое, то выводим его
                System.out.println(i);
            }
        }
    }

    // Функция для проверки, является ли число простым
    public static boolean isPrime(int n) {
        // Если число меньше 2, то оно не является простым
        if (n < 2) {
            return false;
        }
        // Проверяем, делится ли число на какое-либо число от 2 до sqrt(n)
        for (int i = 2; i * i <= n; i++) {
            if (n % i == 0) {
                // Если число делится нацело, то оно не является простым
                return false;
            }
        }
        // Если число не делится нацело ни на какое другое число, то оно простое
        return true;
    }
}
